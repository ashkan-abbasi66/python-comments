def iou_coef(y_true, y_pred, dim=(2,3), epsilon=0.001):
    """
    Shape for y_true and y_pred: NCHW

    Found at
    https://www.kaggle.com/code/awsaf49/uwmgi-unet-train-pytorch
    """
    y_true = y_true.to(torch.float32)
    # y_pred = (y_pred>thr).to(torch.float32) # plt.figure(), plt.imshow(y_pred.squeeze(0).permute(1,2,0).cpu()),plt.show()
    y_pred = y_pred.to(torch.float32)  # plt.figure(), plt.imshow(y_pred.squeeze(0).permute(1,2,0).cpu()),plt.show()
    inter = (y_true*y_pred).sum(dim=dim)
    union = (y_true + y_pred - y_true*y_pred).sum(dim=dim)
    iou = ((inter+epsilon)/(union+epsilon)).mean(dim=(1,0))
    return iou


